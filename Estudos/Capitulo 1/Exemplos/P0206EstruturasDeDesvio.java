public class P0206EstruturasDeDesvio {
    public static void main(String[] args) {
        int a = 25;
        int b = 20;

        // Diretiva If else
        if (a>b) {
            System.out.println("Verdadeiro");
        } else if (a<b){
            System.out.println("Verdadeiro");
        } else {
            System.out.println("Falso");
        }

         // Diretiva Switch

        switch (a) {
            case 1:
                System.out.println("Caso 1");
                break;
            default:
                System.out.println("Caso default");

    }
    }
}
